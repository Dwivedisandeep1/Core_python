# Type Casting :-  Converting one Data Type to another data Type e.g. int to str , str to int etc.
# Explicit Conversion
# Done by Coder
a = "1"
b = "2"
print(int(a) + int(b))

# Implicit type Casting
# one data type is converted to another python interpreter itself

c = 1.9
d = 8

print(c + d)